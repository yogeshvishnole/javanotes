
// Encapsulation is the binding of related data and functions called methods
// using the connstructs like classes.

// Why encapsulation is important

// 1. It groups the related things together so it gives maintainability
// 2. It controls the behavior of data with methods which is generally good and provide us
// more scope of doing different thins

class Studenta{
	private int rollno;
	private String name;
	
	// getters and setters
	public int getRollno() {
		return rollno;
	}
	public void setRollno(int rollno) {
		this.rollno = rollno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}


